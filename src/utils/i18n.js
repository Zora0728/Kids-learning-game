// Helper for bilingual content (titles/descriptions)
export const getTrans = (obj, lang) => {
    if (!obj) return "";
    if (typeof obj === 'string') return obj; // Fallback for legacy string data
    return obj[lang === 'en-US' ? 'en' : 'zh'] || obj['zh'] || "";
};

